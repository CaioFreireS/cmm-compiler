    private void initCustomComponents() {
        this.setLocationRelativeTo(null);
        this.setTitle("IDE C-- Compiler - Sem Título");

        textArea = new RSyntaxTextArea(20, 60);
        textArea.setSyntaxEditingStyle(SyntaxConstants.SYNTAX_STYLE_C);
        textArea.setCodeFoldingEnabled(true);
        textArea.setAntiAliasingEnabled(true);
        textArea.setFont(new java.awt.Font("Consolas", java.awt.Font.PLAIN, 14));
        
        lexerParser = new LexerParser();
        textArea.addParser(lexerParser);

        RTextScrollPane scrollPane = new RTextScrollPane(textArea);
        scrollPane.setFoldIndicatorEnabled(true);
        
        pnlEditorContainer.setLayout(new BorderLayout());
        pnlEditorContainer.add(scrollPane, BorderLayout.CENTER);
        
        configurarRenderizacaoTabela();
        
        Timer timerCompilacao = new Timer(700, e -> atualizarTabelaTokens());
        timerCompilacao.setRepeats(false);

        textArea.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) { verificarECompilar(); }
            @Override
            public void removeUpdate(DocumentEvent e) { verificarECompilar(); }
            @Override
            public void changedUpdate(DocumentEvent e) { verificarECompilar(); }

            private void verificarECompilar() {
                if (mniAutoCompilar.isSelected()) {
                    timerCompilacao.restart();
                }
            }
        });
    }
    
    private void configurarRenderizacaoTabela() {
        tblTokens.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                Object tipoVal = table.getValueAt(row, 2); // Coluna Tipo
                String tipo = (tipoVal != null) ? tipoVal.toString() : "";

                // Pinta Vermelho se for Erro Léxico
                if ("ERROR".equals(tipo)) {
                    c.setForeground(Color.RED);
                    c.setFont(c.getFont().deriveFont(java.awt.Font.BOLD));
                } else {
                    c.setForeground(Color.BLACK);
                }

                // --- Alteração: Pinta Vermelho se for Erro Semântico (na coluna Valor) ---
                Object valorVal = table.getValueAt(row, 4); // Coluna Valor
                String valor = (valorVal != null) ? valorVal.toString() : "";
                if (valor.startsWith("ERRO:")) {
                    c.setForeground(Color.RED);
                }

                return c;
            }
        });
    }

    private void atualizarTabelaTokens() {
        try {
            textArea.forceReparsing(0);
            List<Token> tokens = lexerParser.getLastTokens();

            semanticAnalyzer.analyze(tokens);

            DefaultTableModel model = (DefaultTableModel) tblTokens.getModel();
            model.setRowCount(0);

            if (model.getColumnCount() == 0) {
                model.addColumn("Linha");
                model.addColumn("Coluna");
                model.addColumn("Tipo");
                model.addColumn("Lexema");
                model.addColumn("Valor");
            }

            for (int i = 0; i < tokens.size(); i++) {
                Token t = tokens.get(i);
                String valorColuna = "";

                if (t.getType() == TokenType.ID) {
                    boolean isDeclaration = false;
                    if (i > 0) {
                        TokenType prev = tokens.get(i - 1).getType();
                        if (prev == TokenType.INT || prev == TokenType.VOID || 
                            prev == TokenType.FLOAT || prev == TokenType.CHAR) {
                            isDeclaration = true;
                        }
                    }

                    if (!isDeclaration && !semanticAnalyzer.getSymbolTable().exists(t.getLexeme())) {
                        valorColuna = "ERRO: Não declarado"; 
                    } 
                    else if (semanticAnalyzer.getSymbolTable().exists(t.getLexeme())) {
                        SymbolInfo info = semanticAnalyzer.getSymbolTable().get(t.getLexeme());
                        valorColuna = "Tipo: " + info.type;
                    }
                } 
                else if (t.getType() == TokenType.RETURN && i + 1 < tokens.size()) {
                    Token nextToken = tokens.get(i + 1);
                    String typeError = semanticAnalyzer.checkTypeCompatibility(nextToken);
                    if (typeError != null) {
                        valorColuna = "ERRO: " + typeError;
                    }
                }
                else if (t.getLiteral() != null) {
                    valorColuna = t.getLiteral().toString();
                }

                model.addRow(new Object[]{
                    t.getLine(),
                    t.getColumn(),
                    t.getType(),
                    t.getLexeme(),
                    valorColuna
                });
            }
        } catch (Exception e) {
            System.out.println("Erro ao atualizar tokens: " + e.getMessage());
            e.printStackTrace(); 
        }
    }

    private void acaoCompilar() {
        atualizarTabelaTokens();
    }

    private void acaoAbrir() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivos C-- (*.cmm, *.txt)", "cmm", "txt"));
        
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            try {
                String content = Files.readString(selectedFile.toPath());
                textArea.setText(content);
                textArea.setCaretPosition(0);
                
                arquivoAtual = selectedFile;
                setTitle("IDE C-- Compiler - " + selectedFile.getName());
                
                atualizarTabelaTokens();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao ler arquivo: " + ex.getMessage());
            }
        }
    }

    private void acaoSalvar() {
        if (arquivoAtual == null) {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivos C-- (*.cmm)", "cmm"));
            
            int result = fileChooser.showSaveDialog(this);
            if (result == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();
                if (!fileToSave.getName().toLowerCase().endsWith(".cmm")) {
                    fileToSave = new File(fileToSave.getAbsolutePath() + ".cmm");
                }
                arquivoAtual = fileToSave;
            } else {
                return;
            }
        }
        try {
            Files.writeString(arquivoAtual.toPath(), textArea.getText());
            setTitle("IDE C-- Compiler - " + arquivoAtual.getName());
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + ex.getMessage());
        }
    }
            
    private void acaoNovo() {
        if (textArea.getText().length() > 0) {
            int response = JOptionPane.showConfirmDialog(this, 
                "Deseja salvar o arquivo atual antes de criar um novo?", 
                "Novo Arquivo", 
                JOptionPane.YES_NO_CANCEL_OPTION);
            
            if (response == JOptionPane.CANCEL_OPTION) return;
            if (response == JOptionPane.YES_OPTION) acaoSalvar();
        }

        textArea.setText("");
        arquivoAtual = null;
        setTitle("IDE C-- Compiler - Sem Título");
        
        DefaultTableModel model = (DefaultTableModel) tblTokens.getModel();
        model.setRowCount(0);
    }       
